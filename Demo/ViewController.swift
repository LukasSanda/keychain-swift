import UIKit
import KeychainSwift

let TegKeychainDemo_keyName = "my key"

class ViewController: UIViewController {
  
  @IBOutlet weak var textField: UITextField!
  
  @IBOutlet weak var valueLabel: UILabel!
  
  @IBOutlet weak var synchronizableSwitch: UISwitch!
  
  private var keychain: KeychainSwift {
    KeychainSwift(synchronizable: synchronizableSwitch?.isOn ?? false)
  }

  override func viewDidLoad() {
    super.viewDidLoad()

    updateValueLabel()
  }

  @IBAction func onSaveTapped(_ sender: AnyObject) {
    closeKeyboard()

    if let text = textField.text {
      try? keychain.set(text, forKey: TegKeychainDemo_keyName)
      updateValueLabel()
    }
  }

  @IBAction func onDeleteTapped(_ sender: AnyObject) {
    closeKeyboard()

    try? keychain.delete(TegKeychainDemo_keyName)
    updateValueLabel()
  }

  @IBAction func onGetTapped(_ sender: AnyObject) {
    closeKeyboard()

    updateValueLabel()
  }

  private func updateValueLabel() {
    if let value = try? keychain.get(TegKeychainDemo_keyName) {
      valueLabel.text = "In Keychain: \(value)"
    } else {
      valueLabel.text = "no value in keychain"
    }
  }
  
  private func closeKeyboard() {
    textField.resignFirstResponder()
  }
  
  @IBAction func didTapView(_ sender: AnyObject) {
    closeKeyboard()
  }
}
